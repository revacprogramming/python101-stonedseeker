class Menu:
    dir = {}
    def add(self, name,cost):
        self.dir[name] = cost
    
    def show(self):
        for i in self.dir:
            print(i,self.dir[i])


m = Menu()  # Menu is a class
m.add("Idly", 10)
m.add("Vada",20)


m.show()

